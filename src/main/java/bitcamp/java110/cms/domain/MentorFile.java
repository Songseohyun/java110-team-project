package bitcamp.java110.cms.domain;

public class MentorFile {
  private int mfno;
  private String mfname;
  private int mono;
  
  public int getMfno() {
    return mfno;
  }
  public void setMfno(int mfno) {
    this.mfno = mfno;
  }
  public String getMfname() {
    return mfname;
  }
  public void setMfname(String mfname) {
    this.mfname = mfname;
  }
  public int getMono() {
    return mono;
  }
  public void setMono(int mono) {
    this.mono = mono;
  }
  
}
